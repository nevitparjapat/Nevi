// WAP to check if a number is even or odd.
  
#include<stdio.h>
int main()
{
    int num;
    printf("Enter a Number=");
    scanf("%d",&num);

    if(num%2==0)
    {
        printf("number is Even \n");
    }
    else
    {
        printf("number is Odd \n");
    }
printf("Thank you");
}

